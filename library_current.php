<center><h1 style="background-color:powderblue;">ABOUT INSTITUTE</h1></center>

<h4><p23>
The college was established in 1963 with the name as Malaviya Regional Engineering College, Jaipur as a joint venture of the Government of India and the Government of Rajasthan, Subsequently; on June 26,2002 the college has been given the status of National Institute of Technology and on 15th August 2007, Proclaimed Institute of National Importance through Act of parliament. The Institute is fully funded by Ministry of Human Resource Development (MHRD), Government of India.
</p23><br><br>
<p22>
Extending into an area of over 317 acres of lush greenery, the campus of the Institute is imaginatively laid-out with a picturesque landscape. It presents a spectacle of harmony in modern architecture, and natural beauty which enthrals and inspires.
</p22><br><br>
<p67>
The campus of the institute consisting of the institute buildings, halls of residence and staff colony. It is a residential campus offering accommodation to faculty, staff and students.</p67>
