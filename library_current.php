<center><h1 style="background-color:powderblue;">CENTRAL LIBRARY (Learning Resource Centre) </h1></center>
<h4><p238>
The Central library is located in the Centre of the Institute, where visitors can approach easily. It is well equipped with the new and modern furniture(make Godrej) including optimizers (stacks on tracks). The sitting area of the users is fully air conditioned. Each sitting area/ hall is having different reading cubicles for the users. The Central Library has been reintroduced with new name i.e. "LEARNING RESOURSE CENTRE (LRC)" in April, 2012. Library Database has been computerised through e-Granthalaya Library. Management Software and books are issued through RFID System & SmartCard. it has 15,847 Sq. feet plinth area in two floors (Ground and first). The reading halls of LRC are air conditioned with glass partition Library has separate stack halls for books, periodicals (current and bound volumes), video cassettes, Reference Section and book bank. 
